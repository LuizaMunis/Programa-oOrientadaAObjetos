    private void jRcontador1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRcontador1ActionPerformed
        // TODO add your handling code here:
        if(jTmatricula.getText().equals("")){
        JOptionPane.showMessageDialog(rootPane, "Campo Obrigatorio!");
        jTmatricula.requestFocus();
        }else{
        
        if(jRcontador1.isSelected()){
        v.contador1++;
        } 
        v.setMatricula(Integer.valueOf(jTmatricula.getText()));
        jLcontador1.setText("Votos para 40h: "+v.votos40h());
        jLcontadorVotos.setText("Pessoas que votaram:"+v.contador());
        
            }
        
    }//GEN-LAST:event_jRcontador1ActionPerformed

