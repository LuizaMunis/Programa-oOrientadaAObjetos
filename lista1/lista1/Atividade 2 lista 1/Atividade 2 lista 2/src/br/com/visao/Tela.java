    private void jBresultadoFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBresultadoFinalActionPerformed
        // TODO add your handling code here:    
        if (v.contador1 > v.contador2) {
        JOptionPane.showMessageDialog(null, "Candidato 1 recebeu mais votos.");
         } else if (v.contador2 > v.contador1) {
        JOptionPane.showMessageDialog(null, "Candidato 2 recebeu mais votos.");
        } else {
         JOptionPane.showMessageDialog(null, "Os candidatos receberam a mesma quantidade de votos.");
        }
        
    }//GEN-LAST:event_jBresultadoFinalActionPerformed

